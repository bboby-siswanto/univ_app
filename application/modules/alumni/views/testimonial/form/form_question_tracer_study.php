        function hide_question(element_number) {
            $('#question_' + element_number).addClass('d-none');
        }

        function show_question(element_number) {
            $('#question_' + element_number).removeClass('d-none');
        }

        function reordering_row() {
            var tes = $('table#table_questionaire tbody tr');
            var no = 1;
            for (let i = 0; i < tes.length; i++) {
                var row = $('table#table_questionaire tbody tr').eq(i);
                var class_list = row[0].classList;
                var insert = true;
                $.each(class_list, function(i, v) {
                    if (v == 'd-none') {
                        insert = false;
                    }
                });

                if (insert) {
                    console.log(row[0].classList);
                    $('table#table_questionaire tbody tr').eq(i).find('td:first').text((no++));
                }
            }
        }
    });
